/**
 * @Author daocers
 * @Date 2018/12/27:11:28
 * @Description:
 */
@EnableZuulProxy
@SpringBootApplication
public class ZuulApplication {
    public static void main(String[] args) {
        SpringApplication.run(ZuulApplication.class, args);
    }
}
